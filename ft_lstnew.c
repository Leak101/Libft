#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list		*node;

	if (!(node = malloc(sizeof(*node))))
		return (NULL);
	if (content == NULL)
	{
		node->content = NULL;
		node->content_size = 0;
	}
	else
	{
		node->content = ft_strdup((char *)content);
		node->content_size = content_size;
	}
	node->next = NULL;
	return (node);
}

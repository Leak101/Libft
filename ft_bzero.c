#include "libft.h"

void	ft_bzero(void *s, size_t nbyte)
{
	size_t		i;
	char		*temp;

	i = 0;
	temp = (char *)s;
	while (i < nbyte)
	{
		temp[i] = 0;
		i++;
	}
}

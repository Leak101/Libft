#include "libft.h"

void	ft_putnbr(int nb)
{
	int lim;

	lim = 0;
	if (nb == -2147483648)
	{
		lim = 1;
		nb = nb + 1;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb / 10 != 0)
		ft_putnbr(nb / 10);
	ft_putchar(lim == 1 ? (nb % 10) + 1 + '0' : nb % 10 + '0');
}
